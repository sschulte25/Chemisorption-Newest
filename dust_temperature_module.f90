subroutine trap_int(n, x, f, res)

implicit none

! Inputs
integer, intent(in) :: n
real(double_precision), dimension(n), intent(in) :: x, f

! Outputs
real(double_precision), intent(out) :: res

! Locals
integer :: i
real(double_precision) :: h, int

res = 0.d0
int = 0.d0

do i=1, n-1
  h = abs((x(i+1) - x(i))/2.0)
  int = h * (f(i) + f(i+1))
  res = res + int
enddo

end subroutine trap_int

